int		ft_atoi(const char *str)
{
	int	p;
	int	z;
	int	x;

	p = 0;
	z = 1;
	x = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			z = -z;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		if (x++ >= 18 && z > 0)
			return (-1);
		if (x >= 18 && z < 0)
			return (0);
		p = p * 10 + (*str++ - '0');
	}
	return (p * z);
}
